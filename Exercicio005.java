package exerciciosBasicosParte1;

public class Exercicio005 {
	public static void main(String[] args) {
		/*
		 * Escreva um programa Java que receba dois números como entrada e exiba o
		 * produto de dois números. Dados de teste: Insira o primeiro número: 25 Insira
		 * o segundo número: 5 Saída esperada : 25 x 5 = 125
		 */

		int firstNumber = 25;
		int secondNumber = 5;
		int produt = firstNumber * secondNumber;

		System.out.println(firstNumber + " x " + secondNumber + " = " + produt);
	}
}
